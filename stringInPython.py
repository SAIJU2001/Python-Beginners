#string in python

name=input("Enter your name : ")
print(name)

#uppercase the string
print(name.upper())

#lowercase the string
print(name.lower())

#find index of any character or string 
find=input("Enter your char or string : ")
print(name.find(find))

#replace any character or string 
new_name=input("Enter your char or string : ")
print(name.replace(name,new_name))
 


